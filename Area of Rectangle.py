# Defining a function to calculate the area of a rectangle
def calculate_area(length, width):
    # Calculating the area by multiplying length and width
    area = length * width  

    # Printing the area with a descriptive message
    print(f"The area of the rectangle is {area}")

# Call the function with arguments 5 and 3 for length and width respectively
calculate_area(5, 3)